# Функция для нахождения всех простых чисел от 1 до n
def primes(n):
  # Создаем список чисел от 2 до n
  numbers = list(range(2, n + 1))
  # Создаем пустой список для результата
  result = []
  # Пока список не пустой
  while numbers:
    # Берем первое число в списке
    p = numbers[0]
    # Добавляем его в результат
    result.append(p)
    # Удаляем из списка все числа, которые делятся на p
    numbers = [x for x in numbers if x % p != 0]
  # Возвращаем результат
  return result

# Вводим число n
n = int(input("Введите число n: "))

# Выводим список простых чисел от 1 до n
print("Простые числа от 1 до", n, ":", primes(n))
