# Программа для округления числа до заданного количества знаков после запятой
# Вводим число и количество знаков
number = float(input("Введите число: "))
digits = int(input("Введите количество знаков: "))

# Округляем число с помощью функции round
rounded = round(number, digits)

# Выводим результат
print(f"Округленное число: {rounded}")