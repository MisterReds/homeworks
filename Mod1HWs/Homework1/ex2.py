# Программа для нахождения среднего арифметического двух чисел и округления его до целого числа
# Вводим два числа
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))

# Находим среднее арифметическое с помощью формулы
average = (num1 + num2) / 2

# Округляем среднее до целого числа с помощью функции round
rounded = round(average)

# Выводим результат
print(f"Среднее арифметическое: {average}")
print(f"Округленное среднее: {rounded}")