# импортируем библиотеки pandas и matplotlib.pyplot
import pandas as pd
import matplotlib.pyplot as plt

# скачиваем датасет с данными о продажах домов
# для этого используем функцию pd.read_csv с указанием ссылки на датасет
# также указываем параметр sep=',', так как данные разделены запятой
df = pd.read_csv('Housing.csv', sep=',')

# создаем новый рисунок
plt.figure()

# строим график, на котором будут отмечены все квартиры в виде точек
# для этого используем функцию plt.scatter, которая рисует точки на плоскости с заданными координатами
# указываем столбцы 'price' и 'area' в качестве x и y координат
# также указываем столбец 'parking' в качестве параметра c, который определяет цвет точек
# используем параметр cmap, который задает цветовую карту для точек
# используем параметр alpha, который задает прозрачность точек
plt.scatter(df['price'], df['area'], c=df['parking'], cmap='viridis', alpha=0.5)

# добавляем легенду к графику, которая показывает соответствие цветов и количества парковочных мест
# для этого используем функцию plt.colorbar, которая создает цветовую шкалу
# также указываем параметр label, который задает подпись к цветовой шкале
plt.colorbar(label='Количество парковочных мест')

# подписываем оси координат
plt.xlabel('Цена')
plt.ylabel('Площадь')

# добавляем заголовок к рисунку
plt.title('График квартир по цене и площади')

# показываем рисунок на экране
plt.show()
