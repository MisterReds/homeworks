import numpy as np

# Создаем вектор из 100 случайных чисел от 1 до 10
arr = np.random.randint(1, 11, size=100)

# Сравниваем каждый элемент с 7 и получаем булев вектор
bool_arr = arr > 7

# Считаем количество True в булевом векторе
count = np.count_nonzero(bool_arr)

# Делим количество на размер вектора и умножаем на 100
percent = count / arr.size * 100

# Выводим результат
print(f"Процент элементов в векторе, больших 7, равен {percent:.2f}%")
