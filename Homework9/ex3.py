# Функция для нахождения всех делителей числа n
def divisors(n):
  # Создаем пустой список для результата
  result = []
  # Перебираем все числа от 1 до n
  for i in range(1, n + 1):
    # Если число делит n без остатка
    if n % i == 0:
      # Добавляем его в результат
      result.append(i)
  # Возвращаем результат
  return result

# Вводим число n
n = int(input("Введите число n: "))

# Выводим список делителей числа n
print("Делители числа", n, ":", divisors(n))
