# Функция для нахождения НОД двух чисел
def gcd(a, b):
  while b != 0:
    a, b = b, a % b
  return a

# Функция для нахождения НОК двух чисел
def lcm(a, b):
  return a * b // gcd(a, b)

# Вводим два числа
a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))

# Выводим НОК
print("Наименьшее общее кратное чисел", a, "и", b, "равно", lcm(a, b))
